package persistence;

public class ProxyLoadingException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1195319384664490112L;

	public ProxyLoadingException() {
	}

	public ProxyLoadingException(String arg0) {
		super(arg0);
	}

	public ProxyLoadingException(Throwable arg0) {
		super(arg0);
	}

	public ProxyLoadingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
